package Operators;

public class LearnArithmeticOperator {

    public static void main(String[] args) {
       int num1=40;
       int num2=3;
       int total=num1+num2;
       int balance=num1-num2;
       int monthlyValue=num1*num2;
       int weeklyValue=num1/num2;
       int value=num1%num2;

        System.out.println("Total is : "+total);
        System.out.println("Balance is : "+balance);
        System.out.println("Monthly Value is : "+monthlyValue);
        System.out.println("Weekly Value is : "+weeklyValue);
        System.out.println("Value is : "+value);
    }



}
