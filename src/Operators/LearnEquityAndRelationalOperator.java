package Operators;

public class LearnEquityAndRelationalOperator {

    public static void main(String[] args) {
        int num1 = 50;
        if (num1 == 51) {
            System.out.println("Number1 value is equal to 50");
        }

        if (num1 != 51) {
            System.out.println("Number1 value is not equal to 50");
        }

        if (num1>51){
            System.out.println("Number1 value is more than 50");
        }

        if (num1>=50){
            System.out.println("Number1 value is greater than equal 50");
        }

        if (num1<50){
            System.out.println("Number1 value is less than 50");
        }

        if (num1<=50){
            System.out.println("Number1 value is less than equal 50");
        }



    }
}